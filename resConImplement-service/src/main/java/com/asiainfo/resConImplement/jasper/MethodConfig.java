package com.asiainfo.resConImplement.jasper;
/**
* 类说明：用于存储方法配置
* @author Baomz
* @date 2016年7月21日 下午2:41:19
*/
public class MethodConfig {

	private String method;
	
	private String requestEl;
	
	private String responseEl;

	public String getMethod() {
		return method;
	}

	public void setMethod(String method) {
		this.method = method;
	}

	public String getRequestEl() {
		return requestEl;
	}

	public void setRequestEl(String requestEl) {
		this.requestEl = requestEl;
	}

	public String getResponseEl() {
		return responseEl;
	}

	public void setResponseEl(String responseEl) {
		this.responseEl = responseEl;
	}
	
	
}
